package com.example.axityexamen.Model;

public class ModelProductos {

    public String skuDisplayNameText;
    public String departament;
    public int skuId;
    public Double basePrecio;

    public String getSkuDisplayNameText() {
        return skuDisplayNameText;
    }

    public void setSkuDisplayNameText(String skuDisplayNameText) {
        this.skuDisplayNameText = skuDisplayNameText;
    }

    public String getDepartament() {
        return departament;
    }

    public void setDepartament(String departament) {
        this.departament = departament;
    }

    public int getSkuId() {
        return skuId;
    }

    public void setSkuId(int skuId) {
        this.skuId = skuId;
    }

    public Double getBasePrecio() {
        return basePrecio;
    }

    public void setBasePrecio(Double basePrecio) {
        this.basePrecio = basePrecio;
    }

}
